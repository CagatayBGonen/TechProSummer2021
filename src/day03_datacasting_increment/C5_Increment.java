package day03_datacasting_increment;

public class C5_Increment {

	public static void main(String[] args) {
		
		int num1 = 5;
		
		num1 = num1 + 1;
		
		System.out.println(num1);
		
		// pratik method
		
		num1 += 1;  // += bitisik olamk zorunda
		System.out.println(num1);
		
		int num2 = 10;
		num2 *= 2;   // *= bitisik olmak zorunda
		System.out.println(num2);
		
	}

}
